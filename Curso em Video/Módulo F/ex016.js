let num = [5, 4, 7 , 2, 9];
console.log(`O vetor num é igual a ${num}`);
num[5] = 6; // coloca o número 6 na chave 5;
console.log(`O vetor num é igual a ${num}`);
num.push(1); //Coloca o número 1 na ultima chave;
console.log(`O vetor num é igual a ${num}`);
num.sort(); //Organiza os números em ordem crescente
console.log(`O vetor num é igual a ${num}`);
